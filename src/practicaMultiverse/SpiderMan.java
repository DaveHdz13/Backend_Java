package practicaMultiverse;

public class SpiderMan {
    public String name, alias, gender, suitColor01, suitColor02, gadgets;
    public int earth;
    SpiderMan(String name, String alias, String gender, String suitColor01, String suitColor02, String gadgets, int earth){
        this.name = name;
        this.alias = alias;
        this.gender = gender;
        this.suitColor01 = suitColor01;
        this.suitColor02 = suitColor02;
        this.gadgets = gadgets;
        this.earth = earth;
    }
}
